package com.Composition;

public class Dimensions {
    private int width;
    private int hieght;
    private int depth;

    public Dimensions(int width, int hieght, int depth) {
        this.width = width;
        this.hieght = hieght;
        this.depth = depth;
    }

    public int getWidth() {
        return width;
    }

    public int getHieght() {
        return hieght;
    }

    public int getDepth() {
        return depth;
    }
}
